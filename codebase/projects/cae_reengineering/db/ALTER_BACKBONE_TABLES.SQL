  PATIENT_IDENTIFIER     VARCHAR2(200),
  GENDER                 VARCHAR2(200),
  RACE                   VARCHAR2(200),
  ETHNICITY              VARCHAR2(200)
 );
 
 
 ALTER TABLE SPECIMEN ADD (
  SURGICAL_LABEL         VARCHAR2(200),
  IDENTIFIER             VARCHAR2(200)
 );


ALTER TABLE ACCESSION ADD (
  ACCESSION_DATE             DATE,
  SURGICAL_PATHOLOGY_NUMBER  VARCHAR2(200)
);

CREATE TABLE PARTICIPANT_IDENTIFIERS 
(
 ID NUMBER(10,2),
 PARTICIPANT_ID NUMBER(20)            NOT NULL,
 MEDICAL_RECORD_NUMBER VARCHAR2(200)
);

ALTER TABLE PARTICIPANT_IDENTIFIERS ADD CONSTRAINT FK_PARTICIPANT_IDS  
FOREIGN KEY (PARTICIPANT_ID) REFERENCES PARTICIPANT (ANNOTATABLE_ENTITY_ID)
;


